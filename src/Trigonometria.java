public class Trigonometria extends Calcular
{

    public double Seno(double co, double hp)
    {
        double s;

        s = co / hp;

        return s;
    }

    public double Cosseno(double ca, double hp)
    {

        double c;

        c = ca / hp;

        return c;

    }

    public double Tangente(double co, double ca)
    {
        double t;

        t = co / ca;

        return t;

    }

}
